"""
AgentRoot v5 — OpenClaw Detection Skill
Reads simulation posts from cycle JSON exports (produced by export_to_json.py),
fact-checks each post against ground truth, flags misinformation agents,
and traces them through the ERC-8004 on-chain registry.

Can be run standalone: python openclaw_detect.py
Or via OpenClaw CLI: openclaw agent --message "Run AgentRoot detection"
"""
import json
import os
import sys
import glob
from datetime import datetime

BASE_DIR = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
SHARED_BASE_PATH = os.path.join(BASE_DIR, "knowledge", "shared_base.json")

# Output and input from MiniApp public directory
MINIAPP_DATA = os.path.join(
    BASE_DIR, "..", "world-agents", "packages", "world-mini-app", "public", "data"
)
REPORT_PATH = os.path.join(MINIAPP_DATA, "detection_report.json")

# Agent JSON dir for trace resolution
AGENTS_DIR = os.path.join(BASE_DIR, "agents")


def load_ground_truth():
    """Load Tier 1 shared base as ground truth source material."""
    with open(SHARED_BASE_PATH, "r") as f:
        data = json.load(f)
    return data["facts"]


def load_posts_from_exports():
    """Load all posts from exported cycle JSON files."""
    cycle_files = sorted(glob.glob(os.path.join(MINIAPP_DATA, "cycle_*.json")))
    if not cycle_files:
        print("⚠️  No cycle JSON files found at", MINIAPP_DATA)
        print("   Run simulation/export_to_json.py first.")
        sys.exit(1)

    all_posts = []
    for cf in cycle_files:
        with open(cf, "r", encoding="utf-8") as f:
            posts = json.load(f)
            all_posts.extend(posts)
    return all_posts


